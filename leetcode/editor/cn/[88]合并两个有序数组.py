class Solution(object):
    def merge(self, nums1, m, nums2, n):
        """
        :type nums1: List[int]
        :type m: int
        :type nums2: List[int]
        :type n: int
        :rtype: None Do not return anything, modify nums1 in-place instead.
        """

        for idx, num2 in enumerate(nums2):

            left = 0
            right = m + idx - 1  # å³è¾¹çš„ä½ç½®éšç€æ’å…¥æ•°å­—å+1

            while left <= right:
                mid = int((left + right) / 2)
                if nums1[mid] >= num2:
                    right = mid - 1
                else:
                    left = mid + 1

            for i in range(len(nums1) - 1, left, -1):  # ä»è¦æ’å…¥çš„ç´¢å¼•+1å¤„å¼€å§‹
                nums1[i] = nums1[i - 1]

            nums1[left] = num2


